package RayTracing;

public class RGB {

	private byte red;
	private byte green;
	private byte blue;
	

	public RGB(byte red, byte green, byte blue) {
		this.red = red;
		this.green = green;
		this.blue = blue;
	}
	public byte getRed()
	{
		return red;
	}
	public byte getGreen()
	{
		return green;
	}
	public byte getBlue()
	{
		return blue;
	}
}
